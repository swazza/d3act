/**
 * All styles that can be used to configure the style of the chart or a sub-component of the chart.
 */
export interface IChartStyle {
  /**
   * This group container of the chart is translated by the specified number of pixels along both the X & Y axes.
   */
  margin?: number;

  /**
   * The color with which the chart element is filled
   */
  fill?: string;
}
